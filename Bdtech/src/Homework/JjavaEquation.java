package Homework;

public class JjavaEquation {
	static int a = 10;
	static int b = 15;
	static int c = 20;

	public static void main(String[] args) {
		
		int e = b - a;
		int f = c - b;
	      
	
		System.out.println("b - c = " +e);
		System.out.println("c - b = "  +f);
		multi();
	}
	public static void multi() {
		int g = a * b;
		int h = b * c;
		System.out.println("a * b = "  +g);
		System.out.println("b * c = "  +h);
					 
	}
		
	
		
}


