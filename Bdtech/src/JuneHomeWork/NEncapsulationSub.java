package JuneHomeWork;

public class NEncapsulationSub {
	private String userName;
	private String password;
	private String email;
	private String fistAndLastName;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getFistAndLastName() {
		return fistAndLastName;
	}
	public void setFistAndLastName(String fistAndLastName) {
		this.fistAndLastName = fistAndLastName;
	}
	

}
